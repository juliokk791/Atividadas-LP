    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String serie = serieTextField.getText();
     String nome = nomeTextField.getText();
     String curso = cursoTextField.getText();
     String dataNascimento = dataNascimentoTextField.getText();
      if (serie.isEmpty() || nome.isEmpty() || curso.isEmpty() || dataNascimento.isEmpty()) {
       
        if (serie.isEmpty()) {
            serieLabel.setForeground(Color.RED);
        } else {
            serieLabel.setForeground(Color.GREEN);
        }
        if (nome.isEmpty()) {
            nomeLabel.setForeground(Color.RED);
        } else {
            nomeLabel.setForeground(Color.GREEN);
        }
        if (curso.isEmpty()) {
            cursoLabel.setForeground(Color.RED);
        } else {
            cursoLabel.setForeground(Color.GREEN);
        }
        if (dataNascimento.isEmpty()) {
            dataNascimentoLabel.setForeground(Color.RED);
        } else {
            dataNascimentoLabel.setForeground(Color.GREEN);
        }
        
        
        // Retornar sem realizar o cadastro
        return;
    } 
     
     LocalDate dataNasc = LocalDate.parse(dataNascimento, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
     LocalDate hoje = LocalDate.now();
     Period periodo = Period.between(dataNasc, hoje);
     int idade = periodo.getYears();
     
     if (idade < 18) {
         String responsavel = JOptionPane.showInputDialog(this, "Informe o nome do responsÃ¡vel:");
         if (responsavel != null && !responsavel.isEmpty()) {
             ExibirDadosFrame ExibirDadosFrame = new ExibirDadosFrame();
             ExibirDadosFrame.setDados(nome, serie, curso, dataNascimento, responsavel);
             ExibirDadosFrame.setVisible(true);
         }
     } else {
         ExibirDadosFrame ExibirDadosFrame = new ExibirDadosFrame();
         ExibirDadosFrame.setDados(nome, serie, curso, dataNascimento, "Maior de Idade");
         ExibirDadosFrame.setVisible(true);
     }
           
    }//GEN-LAST:event_jButton1ActionPerformed

